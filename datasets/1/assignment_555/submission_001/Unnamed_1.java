// Base class Vehicle

// Declare the Vehicle class
class Vehicle {
    // Private field to store the speed of the vehicle
    private int speed;

    // Method to increase the speed of the vehicle by 10
    public void speedUp() {
        speed += 10;
    }

    // Method to get the current speed of the vehicle
    public int getSpeed() {
        return speed;
    }
} 
